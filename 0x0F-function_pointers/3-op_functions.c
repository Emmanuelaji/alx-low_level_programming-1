#include "3-calc.h"

/**
 * op_add - su two numbers
 * @a: first num
 * @b: second num;
 * Return: summation
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two numbers
 * @a: first num
 * @b: second num
 * Return: subtaction
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two numbers
 * @a: first num
 * @b: second num
 * Return: multiplication
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two numbers
 * @a: first num
 * @b: second num
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder of two numbers division
 * @a: first num
 * @b: second num
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
